module TestAll

using Test
using UnitConverter

include("util.jl")

@testset "All" begin
    test_convert_unit("km", "m", 1000)
    test_convert_unit("mile", "km", 1.609344)
    test_convert_unit("km/hour", "m/s", 0.27777778)
    test_convert_unit("hm3", "MWh * ((m3/s)/ MW) * ((hm3/hour)/(m3/s))", 1)
    test_convert_unit("k\$", "\$", 1000)
    test_convert_unit("m3/s", "hm3/hour", 0.0036)
    test_convert_unit("hm3", "MWh * ((m3/s)/ MW) * ((hm3/hour)/(m3/s))", 1)
    test_convert_unit("1/GWh", "1/MWh", 0.001)
    test_convert_unit("mi/hour", "m/s", 0.44704)
    test_convert_unit("(m3/s)*(hour)", "hm3", 0.0036)
    test_convert_unit("", "%", 100)
    test_convert_unit("(\$/MWh)/(\$/MWh)", "", 1)
    test_convert_unit("(gal/MWh)*(\$/gal)", "\$/MWh", 1)
    test_convert_unit("(GWh)*(\$/MWh)", "k\$", 1)
    test_convert_unit("\$/MWh", "\$/GWh", 1000)
    test_convert_unit("GWh", "MW * hour", 1000)
    test_convert_unit("kgal", "hm3", 3.7854118e-06)
    test_convert_unit("GWh / hour", "(m3/s)*(MW/(m3/s))", 1000)
    test_convert_unit("GWh", "MW * hour", 1000)
    test_convert_unit("GWh/MW/hour", "%", 100000)
    test_convert_unit("degC", "degF", 1.8)
    test_convert_unit("degF", "K", 0.55555556)
    test_convert_unit("K", "degC", 1)
    test_convert_unit("Pa", "psi", 0.00014503774)
    test_convert_unit("bar", "atm", 0.98692327)
    test_convert_unit("kPa", "mmHg", 7.5006158)
    test_convert_unit("MPa", "psi", 145.03774)
    test_convert_unit("m^2", "ft^2", 10.76391)
    test_convert_unit("km^2", "mi^2", 0.38610216)
    test_convert_unit("hectare", "acre", 2.4710538)
    test_convert_unit("acre", "m^2", 4046.8564)
    test_convert_unit("kg", "lb", 2.2046226)
    test_convert_unit("ton", "kg", 907.18474)
    test_convert_unit("oz", "g", 28.349523)
    test_convert_unit("lb", "ton", 0.0005)
    test_convert_unit("kg/m^3", "lb/ft^3", 0.062427961)
    test_convert_unit("Pa*s", "poise", 10)
    test_convert_unit("W/m^2", "btu/hour/ft^2", 0.31699833)
    test_convert_unit("J/kg/K", "btu/lb/degF", 0.0002388459)
    test_convert_unit("kg/s", "ton/hour", 3.9683207)
    test_convert_unit("m^3/kg", "ft^3/lb", 16.018463)
    test_convert_unit("byte", "bit", 8)
    test_convert_unit("GB", "MB", 1000)
    test_convert_unit("revolutions/minute", "radian/s", 0.10471976)
    test_convert_unit("degree", "radian", 0.017453293)
end

end
